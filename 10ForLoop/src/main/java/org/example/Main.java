package org.example;

public class Main {
    public static void main(String[] args) {

        // for loop = executes a block of code a limited amount of times

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        for (int i = 10; i >= 0; i--) {         // to decrement of increment by more than 1 you do i +=2  or i-+2 to increase or decrease by 2
            System.out.println("Happy new year");
        }


    }
}